/**
 * Renders an icon based on the resolved icon info
 * @param iconInfo - Resolved icon information
 * @returns HTML string for the icon
 */
export function renderIcon(iconInfo: IconInfo): string {
  switch (iconInfo.type) {
    case 'svg':
      return iconInfo.value;
    case 'image':
      return `<img src="${iconInfo.value}" alt="${iconInfo.alt || 'Icon'}" class="w-5 h-5" />`;
    case 'component':
      return iconInfo.value;
    default:
      return iconInfo.value;
  }
}
